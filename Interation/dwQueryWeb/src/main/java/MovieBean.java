public class MovieBean {
    MovieBean(){;}
    private String movieId;
    private String movieName;
    private String movieMPAA;
    private String movieStudio;
    private String movieDuration;
    private String movieTime;
    private String movieStyle;

    public String getMovieStyle() {
        return movieStyle;
    }

    public void setMovieStyle(String movieStyle) {
        this.movieStyle = movieStyle;
    }


    public String getMovieId() {
        return movieId;
    }

    public void setMovieId(String movieId) {
        this.movieId = movieId;
    }

    public String getMovieName() {
        return movieName;
    }

    public void setMovieName(String movieName) {
        this.movieName = movieName;
    }

    public String getMovieMPAA() {
        return movieMPAA;
    }

    public void setMovieMPAA(String movieMPAA) {
        this.movieMPAA = movieMPAA;
    }

    public String getMovieStudio() {
        return movieStudio;
    }

    public void setMovieStudio(String movieStudio) {
        this.movieStudio = movieStudio;
    }

    public String getMovieDuration() {
        return movieDuration;
    }

    public void setMovieDuration(String movieDuration) {
        this.movieDuration = movieDuration;
    }

    public String getMovieTime() {
        return movieTime;
    }

    public void setMovieTime(String movieTime) {
        this.movieTime = movieTime;
    }


}


